package Day03_Variables;

public class EscapeSequences {
	public static void main (String[]args) {
		
		/*\n(or \r): starts from a new line
		 * \t: horizontal tab
		 * \\: to print a backslash on the console
		 * \': single quote
		 * \": prints double quote
		 */
		
		
		System.out.println("Hello Cybertek \nBatch 12 students\n\n\n");
		System.out.print("Tomorrow if off\nand it is true\n");
		System.out.print("Tomorrow if off\n");
		System.out.print("True\n");
		System.out.print("False\n\n\n\n");
		
		System.out.println("\t\t\tClass starts \n\n\n\ntomorrow");
		System.out.println("\\Muhtar");
		System.out.println("\'Java\'\n\n");
		
		System.out.println("Dont forget to buy \"bananas\"");
		
		System.out.println("\"Hello\"");
		System.out.println("\'Hi\'");
		System.out.println("Book is called Game of \"Throne\"");
		
		
		
	}
}
