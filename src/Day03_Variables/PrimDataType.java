package Day03_Variables;

public class PrimDataType {
public static void main (String []args) {
	
	byte length = 3; 
	System.out.println(length);
	
	
	
	byte _Width$ = 4;
	
	System.out.println(_Width$);
	/* variable must start with a letter. 
	 * 
	 */
	
	byte $MyNum1 = 127;
	System.out.println($MyNum1);
	
	long longNumber = 10l;
	System.out.println(longNumber);
	
	byte byteNumber = 100;
	short shortNumber = byteNumber;
	System.out.println(shortNumber);
	
	int intNumber = -19876;
	long longNumber2 = intNumber;
	System.out.println(longNumber2);
	//left side of equal has to be equal or greater than right side
	
	short shortNumber3 = -30000;
	short shortNumber2 = shortNumber3;
	System.out.println(shortNumber3);
	
	short shot1 = 2;
	float floatNum = 10.0f;
	
	
	
}
}
