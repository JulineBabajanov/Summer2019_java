package Day03_Variables;

public class println_vs_print {

	public static void main (String[]args) {
		
		//the only characters you can give is : _ and $
		
		System.out.println("Hello Cybertek"); //prints from a new line
		System.out.println("School"); //println takes place from a next line
		
		System.out.println("======================================================");
		
		System.out.print("Hello Cybertek ");
		System.out.print("School"); // printing takes place from where its left off
		
		System.out.println("===========================================");
		System.out.print("Today is ");
		System.out.println("\ngreat day");
		System.out.print("\t\tHave a nice day");
		
		
		
		
		
		
	}
	
	
}
