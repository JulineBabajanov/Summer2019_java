package day05_Recap;

public class More_Concatenation {
public static void main(String[] args) {
	
	String BookName = "I like the book called \"Game of throne\'";
	System.out.println(BookName);
	String MyInfo = "My name is: \n\t\tCybertek";
	System.out.println(MyInfo);
	System.out.println('3'+3);  //54
	System.out.println("3"+'3'); // if we concat char to String, char is concated as character  33
	System.out.println(12+'3'); // if we concat char to a number, representive number of char will be concated  63
	System.out.println("2"+3); //23
	
	System.out.println('7'+9); //64 = 55+9
	
}
}
