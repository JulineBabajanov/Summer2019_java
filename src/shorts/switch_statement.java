package shorts;

public class switch_statement {
public static void main(String[] args) {
	
	int restrauntRating = 10;
	
	switch(restrauntRating) {
	case 1:
		System.out.println("This restraunt is not my favorite");
		break;
	case 2:
		System.out.println("This restraunt is good");
		break;
	case 3:
		System.out.println("This restraunt is fantastic");
		break;
	default:
		System.out.println("I have never dined in this restraunt before");
		break;
	}
	
	
	
}
}
