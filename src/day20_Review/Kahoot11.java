package day20_Review;

public class Kahoot11 {
public static void main(String[] args) {
	
	//q11
	/*
	 * for(initiliziation;condition;iterator)
	 */
	int i =0;
	for(i=5; i<10;) {   //i: 5. 7, 9
		i+=2;          //final value of i: 9+2=11
	}
	System.out.println(i);
	
	
	
	for(int j = 1; j<5;) {
		//j++
		System.out.print(j+" ");
		j++;
	}
	
	
}
}
