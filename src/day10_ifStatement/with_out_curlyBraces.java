package day10_ifStatement;

public class with_out_curlyBraces {
public static void main(String[] args) {
	
	/* single if statement :
	 *  if(condition) {
	 *          //some codes
	 * }
	 * 
	 * 
	 */
	if(false) {
		System.out.println("Today is Friday");
		System.out.println("Tomorrow is day off");
		System.out.println("There is no java class tomorrow");
	}
	
	/* if statement without curly braces:
	 * 
	 * single if statement:
	 * if(condition)
	 *     only a single line of code statement
	 *     
	 */
	
	
	
	if(false)
		System.out.println("Today is Friday");
	
	System.out.println("Tomorrow there is no class");
	
 if (true)
	 System.out.println("Hello");
	
 System.out.println("World :)");
 
 System.out.println("HOw are you");

 /* if(condition)
  *    
  * 
  * 
  * 
  */
	if(false)
		System.out.println("Hello");
	else
		System.out.println("Hola");
	
	/* multi-branch if statement w/o curly braces:
	 * 
	 * if(condition1)
	 *     single if statement only
	 *     else if (condition2)
	 *     single line of statement
	 *     else 
	 *     single line of statement
	 */

	 if(false)
		 System.out.println("if block");
	 else if(false)
	System.out.println("else-if block");
	 else
		 System.out.println("else block");
	
	
	
}
}
